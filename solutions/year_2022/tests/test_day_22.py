def test_faces() -> None:
    """Test all transitions by moving to the edge and take 1 step."""
    cube: Cube = Cube.from_text(EMPTY_CUBE_LARGE, 50)
    instructions = Instructions.from_text("1")

    # FACE 1

    cube.position = cube.face_1.top_left
    cube.facing = Facing.LEFT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_4.bottom_left

    cube.position = cube.face_1.top_left
    cube.facing = Facing.TOP
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_6.top_left

    cube.position = cube.face_1.bottom_right
    cube.facing = Facing.RIGHT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_2.bottom_left

    cube.position = cube.face_1.bottom_right
    cube.facing = Facing.BOTTOM
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_3.top_right

    # FACE 2

    cube.position = cube.face_2.top_left
    cube.facing = Facing.LEFT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_1.top_right

    cube.position = cube.face_2.top_left
    cube.facing = Facing.TOP
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_6.bottom_left

    cube.position = cube.face_2.bottom_right
    cube.facing = Facing.RIGHT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_5.top_right

    cube.position = cube.face_2.bottom_right
    cube.facing = Facing.BOTTOM
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_3.bottom_right

    # FACE 3

    cube.position = cube.face_3.top_left
    cube.facing = Facing.LEFT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_4.top_left

    cube.position = cube.face_3.bottom_right
    cube.facing = Facing.RIGHT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_2.bottom_right

    # FACE 4

    cube.position = cube.face_4.top_left
    cube.facing = Facing.LEFT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_1.bottom_left

    cube.position = cube.face_4.top_left
    cube.facing = Facing.TOP
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_3.top_left

    # FACE 5

    cube.position = cube.face_5.bottom_right
    cube.facing = Facing.RIGHT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_2.top_right

    cube.position = cube.face_5.bottom_right
    cube.facing = Facing.BOTTOM
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_6.bottom_right

    # FACE 6

    cube.position = cube.face_6.top_left
    cube.facing = Facing.LEFT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_1.top_left

    cube.position = cube.face_6.bottom_right
    cube.facing = Facing.RIGHT
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_5.bottom_right

    cube.position = cube.face_6.bottom_right
    cube.facing = Facing.BOTTOM
    cube.follow_instructions(instructions)

    assert cube.position == cube.face_2.top_right
